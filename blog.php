                           <p>Microsoft PowerPoint has been around since 1987 and is by far the most popular presentation tool. It is useful to present a hot new startup idea or for presenting your marketing strategies. Whether you are a newbie or a pro, here are some cool PowerPoint tips and tricks that will be handy for your next presentation.
                           </p>
                           <p>
                              <b>1. More image, less text</b> <br>
                              A picture can communicate more effectively as compared to text. Relevant and high-quality images will keep the audience engaged. Keeping words to a minimum ensures that the audience does not lose focus.

                           </p>
