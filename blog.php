                           <b>2.Design matters</b><br>
                           Follow a consistent design throughout all the slides.
                            Self-designed templates and graphics, simple fonts which complement the templates will showcase a professional look. Statistics show that stock prices of those corporates are likely to increase which flush bad PowerPoint and focus on designing their stories well.<br><br>
                            
                           <b>3.No paragraphs</b><br>
                           Bulk of information presented in a paragraph format is more likely to leave your audience drained of energy. Ideally in presentations, one should keep the text in bullet format. <br>
                           6×6 Rule states that every slide must have 6 bullet points and 6 words in every bullet point.<br><br>
                            
                           <b>4.Use appropriate colors</b><br>
                           Colors evoke feelings. Using standard colors is always preferable but using the color that represents your brand is also a good idea. A perfect balance of light and dark colors gives a visually appealing look. Light background with dark text works amazing if you plan to keep lights on while presenting. Light text on a dark background works fine if you will be presenting in a dark room.<br><br>
                            
                           <b>5.Keep it simple</b><br>
                           Less is more. While making your presentation always focus on a clear message rather than cluttered with unnecessary information. Avoid over formatting at all costs. Use simple and relevant charts that are easy to understand.<br><br>

                           <b>6.Hire a design agency</b><br>
                           A professional agency will always have an upper edge in making a PowerPoint presentation that will stand out. <br>
